public class Lab8 {
    static double aitken(double x0, int n, int m, double[] x, double[] y) {
        if (m == n) return y[n];
        double a1 = x0 - x[n];
        double a2 = x0 - x[m];
        double b1 = aitken(x0, n + 1, m, x, y);
        double b2 = aitken(x0, n, m - 1, x, y);
        double dx = x[m] - x[n];
        return (a1 * b1 - a2 * b2) / dx;
    }

    public static void main(String[] args) {
        double[] x = {0.2376, 0.7368, 1.1448, 1.9872, 2.5392, 2.7648, 3.0616, 3.2088, 3.2784, 3.6904, 3.9368};
        double[] y = {-3.7117, -0.7525,  0.2297, 0.0128, -0.3824, -0.3175, 0.1348, 0.5575, 0.8109, 3.1398, 5.3285};
        double x0 = 2.2248;
        double res_x[] = new double[55];
        res_x[0] = 0.2376; res_x[1] = 0.7368; res_x[2] = 1.1448; res_x[3] = 1.9872;
        double res_y[] = new double[55];
        res_y[0] = -3.7117; res_y[1] = -0.7525; res_y[2] = 0.2297; res_y[3] = 0.0128;
        int i = 4;
        for (int k = 0; k <= 50; k++) {
            double xn = x0 + 0.1 * k;
            res_x[i] = xn;
            res_y[i] = aitken(xn, 0, 10, x, y);
            i++;
        }
        for (int k = 0; k < 55; k++) {
            System.out.print(res_x[k] + ", ");
        }
        System.out.println();
        for (int k = 0; k < 55; k++) {
            System.out.print(res_y[k] + ", ");
        }
    }
}
